def solve(N, M, l):
    ans = [0] * N
    for m in range(M):
        for c in l[m]:
            ans[c-1] += 1

    return ans

print(solve(4, 3, [[1, 2], [2, 3], [1, 4]]) == [2, 2, 1, 1])
print(solve(2, 5, [[1, 2], [2, 1], [1, 2], [2, 1], [1, 2]]) == [5, 5])
print(solve(8, 8, [[1, 2], [3, 4], [1, 5], [2, 8], [3, 7], [5, 2], [4, 1], [6, 8]]) == [3, 3, 2, 2, 2, 1, 1, 2])
